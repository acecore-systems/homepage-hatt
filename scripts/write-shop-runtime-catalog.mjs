import { mkdir, readFile, readdir, writeFile } from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..')
const productsDir = path.join(root, 'src', 'content', 'products')
const settingsPath = path.join(
  root,
  'src',
  'content',
  'shop-settings',
  'main.json',
)
const outputPath = path.join(
  root,
  'functions',
  'api',
  'shop',
  '_catalog.generated.ts',
)

async function readJson(filePath) {
  return JSON.parse(await readFile(filePath, 'utf8'))
}

const productFiles = (await readdir(productsDir))
  .filter((file) => file.endsWith('.json'))
  .sort((a, b) => a.localeCompare(b))
const products = await Promise.all(
  productFiles.map((file) => readJson(path.join(productsDir, file))),
)
const settings = await readJson(settingsPath)
const body = `// This file is generated by scripts/write-shop-runtime-catalog.mjs.
// Do not edit it by hand.

export const SHOP_PRODUCTS = ${JSON.stringify(products, null, 2)} as const

export const SHOP_SETTINGS = ${JSON.stringify(settings, null, 2)} as const
`

await mkdir(path.dirname(outputPath), { recursive: true })
await writeFile(outputPath, body, 'utf8')
